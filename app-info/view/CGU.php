<h3>Article n° 1 : Objet</h2>

<p>Les conditions générales de vente décrites ci-après détaillent les droits et obligations de la société GreenSwitch et de son client dans le cadre de la vente des marchandises suivantes : fruits et légumes (le vendeur doit recenser les marchandises soumises aux CGV).</p>

<p>Toute prestation accomplie par la société GreenSwitch implique donc l'adhésion sans réserve de l'acheteur aux présentes conditions générales de vente.</p>

<h3>Article n° 2 : Prix</h2>

<p>Les prix des marchandises vendues sont ceux en vigueur au jour de la prise de commande. Ils sont libellés en euros et calculés hors taxes. Par voie de conséquence, ils seront majorés du taux de TVA et des frais de transport applicables au jour de la commande.</p> 


<h3>Article n° 3 : Rabais et ristournes</h3>

<p>Les tarifs proposés comprennent les rabais et ristournes que la société GreenSwitch serait amenée à octroyer compte tenu de ses résultats ou de la prise en charge par l'acheteur de certaines prestations.</p>

<h3>Article n° 4 : Escompte</h3>

<p>Aucun escompte ne sera consenti en cas de paiement anticipé.</p>

<h3>Article n° 5 : Modalités de paiement</h3>

<p>Le règlement des commandes s'effectue :</p>
	<li>soit par chèque ;</li>
	<li>soit par carte bancaire ;</li>
	<li>le cas échéant, indiquer les autres moyens de paiement acceptés.</li>

<p>Lors de l'enregistrement de la commande, l'acheteur devra verser un acompte de 10% du montant global de la facture, le solde devant être payé à réception des marchandises.</p>

<h3>Article n° 6 : Retard de paiement</h3>

<p>En cas de défaut de paiement total ou partiel des marchandises livrées au jour de la réception, l'acheteur doit verser à la société GreenSwitch une pénalité de retard égale à trois fois le taux de l'intérêt légal.</p>

<p>Le taux de l'intérêt légal retenu est celui en vigueur au jour de la livraison des marchandises.</p>

<h3>Article n° 7 : Clause résolutoire</h3>

<p>Si dans les quinze jours qui suivent la mise en oeuvre de la clause " Retard de paiement ", l'acheteur ne s'est pas acquitté des sommes restant dues, la vente sera résolue de plein droit et pourra ouvrir droit à l'allocation de dommages et intérêts au profit de la société GreenSwitch.</p>

<h3>Article n° 8 : Clause de réserve de propriété</h3>

<p>La société GreenSwitch conserve la propriété des biens vendus jusqu'au paiement intégral du prix, en principal et en accessoires. À ce titre, si l'acheteur fait l'objet d'un redressement ou d'une liquidation judiciaire, la société ... (dénomination sociale) se réserve le droit de revendiquer, dans le cadre de la procédure collective, les marchandises vendues et restées impayées.</p>

<h3>Article n° 9 : Livraison</h3>

<p>La livraison est effectuée :</p> 
	<li>soit par la remise directe de la marchandise à l'acheteur ;
	<li>soit par l'envoi d'un avis de mise à disposition en magasin à l'attention de l'acheteur ;
	<li>soit au lieu indiqué par l'acheteur sur le bon de commande.</li>

<p>Le délai de livraison indiqué lors de l'enregistrement de la commande n'est donné qu'à titre indicatif et n'est aucunement garanti.<p>
 
<p>Par voie de conséquence, tout retard raisonnable dans la livraison des produits ne pourra pas donner lieu au profit de l'acheteur à :</p> 
	<li>l'allocation de dommages et intérêts ;</li>
	<li>l'annulation de la commande.</li>
	<li>Le risque du transport est supporté en totalité par l'acheteur.</li> 

<p>En cas de marchandises manquantes ou détériorées lors du transport, l'acheteur devra formuler toutes les réserves nécessaires sur le bon de commande à réception desdites marchandises. Ces réserves devront être, en outre, confirmées par écrit dans les cinq jours suivant la livraison, par courrier recommandé AR.</p>


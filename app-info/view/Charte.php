Si aucun message concernant votre problème n'a été posté préalablement, il vous suffit de poster un nouveau message en choisissant le forum approprié.</p>

 
<h3>Style</h3>

<p>Les échanges sur un forum sont radicalement différents des échanges traditionnels dans la mesure où les messages sont textuels et souvent rédigés rapidement.
Ainsi, un message rédigé en majuscules « donne l'impression » d'une hausse de ton et est souvent interprété à tort comme signe d'énervement. Il en est de même pour un message suivi d'un grand nombre de caractères de ponctuation. Il est donc recommandé de faire un usage modéré de ce type d'artifice.</p>
 
<p>Voici un exemple de message à éviter :
POUVEZ-VOUS ME DONNER LA REPONSE ?!?!?!???</p>
 
<p>Il est également demandé aux utilisateurs de s'appliquer lors de la rédaction de leur message en évitant le plus possible les abréviations (type SMS) et en vérifiant l'orthographe. Un message rédigé en langage abrégé ou difficile à lire compte tenu du nombre de fautes d'orthographe est susceptible d'être supprimé afin de garantir une qualité de lecture des échanges.</p>
 
 
<h3>Titre du message</h3>
 

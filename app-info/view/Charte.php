<h3>Conseils d'écriture d'un message</h3>

<p>Avant toute publication d’un article, d’une actualité, d’une fiche documentaire ou d’un message dans les forums, il est recommandé d'utiliser le moteur de recherche afin de vérifier si une publication équivalente n'a pas été déposée auparavant.

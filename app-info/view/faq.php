<h3>Pourquoi ce nom ?</h3>
<p>« GreenSwitch » est un savant mélange anglais entre la couleur verte (« Green ») rappelant les fruits et légumes, la nature ou même le jardinage et l’échange (« switch ») symbolisant l’essence même de notre site : partager et échanger des produits cultivés chez-soi.</p>

<h3>Just switch it ?</h3>
<p>Qui n’a jamais entendu parler du slogan de la célèbre marque à la virgule. Notre slogan en est largement inspiré. Tout est dans la citation (« il suffit de l’échanger »), Greenswitch vous met simplement et rapidement en relation avec des particuliers entretenant un potager et souhaitant tout comme vous échanger leurs produits.</p>

<h3>Comment utiliser « GreenSwitch » ?</h3>
<p>Notre site est consultable sans avoir à s’inscrire : vous pouvez afficher les annonces présentes sur notre site pour vous faire une idée de ce qu’il peut y avoir sur notre plateforme d’échange. En revanche il est nécessaire de créer un compte utilisateur pour pouvoir répondre à une annonce ou en poster une.
Vous trouverez ces fonctionnalités dans les onglets situés dans la barre de menu horizontale.</p>

<h3>Comment rechercher une annonce ?</h3>
<p>Que vous soyez visiteur ou utilisateur du site, vous disposez d’un bouton « Rechercher » dans la barre d’onglets horizontale. Cliquez dessus pour accéder à la page de recherche d’un produit. Entrez-y un mot clef (de 4 lettres minimum) pour afficher toutes les annonces dont le titre contient le contenu de votre recherche. Vous avez également la possibilité d’appliquer un filtre à votre recherche (rechercher dans une région, ou une certaine catégorie de produit).</p>

<h3>Comment répondre à une annonce ?</h3>
<p>Lorsque vous vous trouvez sur la page d’une annonce, cliquez sur « Envoyez un mail au vendeur» afin de contacter l’auteur de l’annonce et définir avec lui les modalités de l’échange.</p>

<h3>Comment poster une annonce ?</h3>
<p>L’onglet "poster une annonce" permet de rentrer un produit que vous proposerez aux autres utilisateurs. Cette fonctionnalité est résérvée aux membres du site. Vous pourrez y rentrer toutes les informations nécessaires à la bonne compréhension de votre annonce par les autres utilisateurs. </p>

<h3>Comment communiquer entre utilisateurs ?</h3>
<p>L’onglet message permet d’envoyer un message privé à n’importe quel utilisateur de GreenSwitch. Vous pouvez également y consulter vos messages reçus/envoyés de manière simple. N’hésitez pas à utiliser  ce système de messagerie afin de poser vos questions aux vendeurs si vous souhaitez obtenir des informations complémentaires sur un produit.</p>

<h3>Un problème ?</h3>
<p>Si vous rencontrez un problème, une question, vous pouvez nous contacter via le lien « Contact » situé en bas de chaque page du site. Nous recevrons votre requête par le biais d’un courriel et nous ferons tout notre possible pour vous aider dans les plus brefs délais.</p>

<h3>Comment se passe le paiement ?</h3>
<p>Si votre échange est monnayé, GreenSwitch n’intervient en aucun cas dans la transaction. L’échange se fait entre particuliers et c’est à vous de convenir avec le vendeur/acheteur des modalités de paiement. Pour l’instant, il n’existe pas de module de paiement sur notre site.</p>

